#!/usr/bin/perl

use warnings;
use strict;

use Data::Dumper;

my(@g); # Our graph as a 2 dimensional array

my($row)=0;
my($start);

while(<DATA>){
  chomp;
  $g[$row]=[];
  my($c)=0;
  foreach(split //){
    if($_ eq 'S'){
      $start = { y=>$row, x => $c};
      push @{$g[$row]}, { label => $_, height => 'a', visited => 0 };
    }elsif($_ eq 'E'){
      push @{$g[$row]}, { label => $_, height => 'z', visited => 0 };
    }else{
      push @{$g[$row]}, { label => $_, height => $_ , visited => 0 };
    }
    $c++;
  }
  $row++;
}

# convenience var for L,R,U,D
my(@edges) = ( 
  [-1, 0],
  [ 1, 0],
  [ 0,-1],
  [ 0, 1]
);

my($end)=findRoutes($start);
print $end->{distance};

sub findRoutes{
  my($start)=shift;
  my(@q);
  $start->{parent}=undef;
  $start->{distance}=0;
  unshift(@q,$start);
  while($#q >=0){
    my($v)=shift(@q);
    if($g[$v->{y}][$v->{x}]->{label} eq 'E'){
       return $v;
    }else{;
      foreach(@edges){
        my($edge)=$_;
        my($w)={};
        $w->{x}=$v->{x}+$edge->[0];
        $w->{y}=$v->{y}+$edge->[1];
        # check we havent walked off the edge
        unless(($w->{x}<0)||($w->{y}<0)||($w->{y}>$#g)||($w->{x}>$#{$g[$w->{y}]})){
          # check we didn;t process this location already
          unless($g[$w->{y}][$w->{x}]->{visited}){
            if((ord($g[$w->{y}][$w->{x}]->{height})-ord($g[$v->{y}][$v->{x}]->{height}))<=1){
              $g[$w->{y}][$w->{x}]->{visited} = 1;
              $w->{distance}=$v->{distance}+1;
              $w->{parent} = $v;
              push(@q,$w);
            }
          }
        }
      }
    }
  }
}


